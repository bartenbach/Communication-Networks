import java.util.List;

public class KnockKnockProtocol {

    private static final int WAITING = 0;
    private static final int SENTKNOCKKNOCK = 1;
    private static final int SENTCLUE = 2;
    private static final int ANOTHER = 3;
    private int state = WAITING;

    private Joke currentJoke;
    private int currentJokeIndex = 0;
    private int NUMJOKES;
    private List<Joke> jokeList;

    public KnockKnockProtocol(List<Joke> jokeList) {
        this.jokeList = jokeList;
        this.NUMJOKES = jokeList.size();
        this.currentJoke = jokeList.get(currentJokeIndex);
    }

    public String processInput(String theInput) {
        String theOutput = null;

        if (state == WAITING) {
            theOutput = "Knock! Knock!";
            state = SENTKNOCKKNOCK;
        } else if (state == SENTKNOCKKNOCK) {
            if (theInput.equalsIgnoreCase("Who's there?")) {
                theOutput = currentJoke.getClue();
                state = SENTCLUE;
            } else {
                theOutput = "You're supposed to say \"Who's there?\"! " +
                        "Try again. Knock! Knock!";
            }
        } else if (state == SENTCLUE) {
            if (theInput.equalsIgnoreCase(currentJoke.getClue() + " who?")) {
                theOutput = currentJoke.getPunchline() + " Want another? (y/n)";
                state = ANOTHER;
            } else {
                theOutput = "You're supposed to say \"" +
                        currentJoke.getClue() +
                        " who?\"" +
                        "! Try again. Knock! Knock!";
                state = SENTKNOCKKNOCK;
            }
        } else if (state == ANOTHER) {
            if (theInput.equalsIgnoreCase("y")) {
                theOutput = "Knock! Knock!";
                if (currentJokeIndex == (NUMJOKES - 1)) {
                    currentJokeIndex = 0;
                    currentJoke = jokeList.get(0);
                } else {
                    currentJokeIndex++;
                    currentJoke = jokeList.get(currentJokeIndex);
                }
                state = SENTKNOCKKNOCK;
            } else {
                theOutput = "Bye.";
                state = WAITING;
            }
        }
        return theOutput;
    }
}